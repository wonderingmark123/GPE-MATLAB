function make_images_z(dt)
f=figure();
%dt=0.5;
h=0.25;
hz=0.25;
N=256;
Nz=64;
r=h*((1:N)-N/2-0.5);
rz=hz*((1:Nz)-Nz/2-0.5);
niter_outer=1000;
load(sprintf('slicez_%03d.mat',0));
maxx = max(max(abs(slicez(:,:))));
%maxx = 39;
for j=0:niter_outer
    load(sprintf('slicez_%03d.mat',j));
    clf(f,'reset')
    axes('Position',[0.2,0.1,0.62,0.84])
    imagesc(r,rz,angle(slicez.'));
    set(gca,'YDir','normal');
    annotation(f,'textbox',...
        [0.1246428571428571 0.460428571428572 0.0482142857142857 0.0833333333333334],...
        'String',{'Z'},...
        'FontSize',14,...
        'FitBoxToText','on',...
        'LineStyle','none');
    annotation(f,'textbox',...
        [0.496785714285713 0 0.0482142857142857 0.0690476190476191],...
        'String',{'X'},...
        'FontSize',14,...
        'FitBoxToText','on',...
        'LineStyle','none');
    annotation(f,'textbox',...
        [0.680541991981428 0.788961038961044 0.21092023633678 0.127424242424244],...
        'String',{sprintf('t = %0.1f',(j*dt))},...
        'FontWeight','bold',...
        'FontSize',20,...
        'FitBoxToText','on',...
        'LineStyle','none',...
        'Color',[0 0 0]);
    saveas(f,sprintf('phasez_%03d.png',j));
    image(r,rz,abs(slicez.')/maxx*64);
    set(gca,'YDir','normal');
    annotation(f,'textbox',...
        [0.680541991981428 0.788961038961044 0.21092023633678 0.127424242424244],...
        'String',{sprintf('t = %0.1f',(j*dt))},...
        'FontWeight','bold',...
        'FontSize',20,...
        'FitBoxToText','on',...
        'LineStyle','none',...
        'Color',[1 1 0]);    
    saveas(f,sprintf('solz_%03d.png',j));
end